
//prints the sum of x ranging from 1 to 20
public class Range {

        public static void main(String[] args) {
            int sum = 0;
            for (int x = 1; x <= 20; x++) {
                sum += x;
            }
            System.out.println("The sum of x ranging from 1 to 20 is " + sum);
        }
    }

